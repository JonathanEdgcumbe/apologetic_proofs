package com.example.jono.apologeticproofs.DivineAttributesPackage;

import android.os.Bundle;


public class Omnibenevolence extends BaseActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setTitle("Omnibenevolence");
        addHeading("Description");
        addNormalText("Not only is God extremely powerful, but He also has the property of being " +
                "omnibenevolent. To be omnibenevolent is to be all-good or all-loving, and" +
                " this means several things." +
                "<br><br>" +
                "Firstly, it means that God loves you dearly. John 3:16–17 (ESV) says,");
        addBlockQuote("For God so loved the world, that he gave his only Son, that whoever" +
                " believes in him should not perish but have eternal life. For God did not" +
                " send his Son into the world to condemn the world, but in order that the " +
                "world might be saved through him.<br>");
        addNormalText("The fact that Jesus laid down his life for you demonstrates the " +
                "incredible, unselfish love that God has for you." +
                "<br><br>" +
                "Secondly, God is gracious towards you. This means that God does not deal " +
                "with you according to your worthiness or what you have done. Instead, God " +
                "deals with you according to your needs and His goodness. As Paul writes in " +
                "Ephesians 2:8–9 (ESV): “For by grace you have been saved through faith. And " +
                "this is not your own doing; it is the gift of God, not a result of works, so " +
                "that no one may boast”." +
                "<br><br>" +
                "Thirdly, God is merciful. God has a tender-hearted compassion for " +
                "His people. We see this, for example, in Mark 1:40–42 (ESV), which reads:");
        addBlockQuote("And a leper came to [Jesus], imploring him, and kneeling said to him, " +
                "“If you will, you can make me clean.” Moved with pity, [Jesus] stretched out " +
                "his hand and touched him and said to him, “I will; be clean.” And immediately " +
                "the leprosy left him, and he was made clean.<br>");
        addNormalText("Finally, God’s omnibenevolence includes His being faithful." +
                " In other words, God keeps all His promises. As Paul says in 1 Thessalonians" +
                " 5:24 (ESV): “He who calls you is faithful; he will surely do it”. So when we " +
                "say that God is omnibenovelnet, we mean that He is loving, gracious, merciful," +
                " and faithful.");
    }
}

